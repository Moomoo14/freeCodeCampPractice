// 举例
var ourArray = ["Stimpson", "J", "cat"];
ourArray.shift(); // 经过 shift 操作后，ourArray 的值为 ["J", "cat"]
ourArray.unshift("Happy");
// 经过 unshift 操作后，ourArray 的值为 ["Happy", "J", "cat"]

